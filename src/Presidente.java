public class Presidente {
    private String nombreClub;
    private String nombrePresi;
    private int    codigoPresi;

    public String getNombreClub() {
        return nombreClub;
    }

    public void setNombreClub(String nombreClub) {
        this.nombreClub = nombreClub;
    }

    public String getNombrePresi() {
        return nombrePresi;
    }

    public void setNombrePresi(String nombrePresi) {
        this.nombrePresi = nombrePresi;
    }

    public int getCodigoPresi() {
        return codigoPresi;
    }

    public void setCodigoPresi(int codigoPresi) {
        this.codigoPresi = codigoPresi;
    }
}
